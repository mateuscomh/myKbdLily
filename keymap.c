// Função para desenhar a BARRA DE WPM VERTICAL que cresce de baixo para cima
static void draw_vertical_wpm_bar(void) {
    uint8_t wpm = get_current_wpm();
    const uint8_t peak_wpm = 150;
    // A área da barra tem 104 pixels de altura (13 linhas de 8px)
    const uint8_t bar_pixel_height_max = 104;

    // Calcula a altura atual da barra em pixels
    uint8_t bar_pixel_height = (wpm > peak_wpm) ? bar_pixel_height_max : (wpm * bar_pixel_height_max) / peak_wpm;

    // Calcula quantos blocos de 8 pixels estão completamente preenchidos
    uint8_t full_blocks = bar_pixel_height / 8;
    // Calcula quantos pixels sobram para o bloco do topo
    uint8_t partial_pixels = bar_pixel_height % 8;

    // Desenha os blocos completos de baixo para cima
    for (uint8_t i = 0; i < full_blocks; i++) {
        oled_set_cursor(3, 15 - i); // Começa na linha 15 e sobe
        oled_write_raw_P(&vertical_bar_segments[8], 1); // Pega o caractere de bloco cheio (0xFF)
    }

    // Desenha o bloco parcial no topo da barra
    if (partial_pixels > 0 && full_blocks < 13) {
        oled_set_cursor(3, 15 - full_blocks);
        oled_write_raw_P(&vertical_bar_segments[partial_pixels], 1);
    }
}

// Função de inicialização do OLED
oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    if (!is_keyboard_master()) {
        return OLED_ROTATION_180;
    }
    // Define e retorna a rotação vertical. Se ficar de cabeça para baixo, troque para OLED_ROTATION_90.
    return OLED_ROTATION_270;
}

// Renderiza o status no OLED (lado esquerdo) - VERSÃO VERTICAL FINAL
static void render_status(void) {
    oled_clear();

    // --- Coluna de Texto (à Esquerda) ---
    // Informações de Camada
    oled_set_cursor(0, 0);
    oled_write_P(PSTR("LAYER:"), false);
    oled_set_cursor(0, 1);
    switch (get_highest_layer(layer_state)) {
        case _LOWER:  oled_write_P(PSTR("Lower"), false); break;
        case _RAISE:  oled_write_P(PSTR("Raise"), false); break;
        case _ADJUST: oled_write_P(PSTR("Adjust"), false); break;
        default:      oled_write_P(PSTR("Base"), false); break;
    }

    // WPM (Texto)
    oled_set_cursor(0, 3);
    oled_write_P(PSTR("WPM:"), false);
    oled_set_cursor(0, 4);
    sprintf(wpm_str, "%03d", get_current_wpm());
    oled_write(wpm_str, false);

    // Status do Caps Lock
    led_t led_state = host_keyboard_led_state();
    if (led_state.caps_lock) {
        oled_set_cursor(0, 6);
        oled_write_P(PSTR("CAPS"), false);
    }

    // --- Coluna da Barra (à Direita) ---
    draw_vertical_wpm_bar();
}


