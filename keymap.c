// Funções de Teclas Personalizadas
// =============================================
// CAPS Switch

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    static uint16_t tab_timer;

    switch (keycode) {
        case CAPS_HOLD:
            if (record->event.pressed) {
                tab_timer = timer_read();
            } else {
                if (timer_elapsed(tab_timer) < TAPPING_TERM) {
                    tap_code(KC_NUBS);
                } else {
                    tap_code(KC_CAPS);
                }
            }
            return false; // Tecla personalizada tratada
    }
    return true; // Processar outras teclas normalmente
}
