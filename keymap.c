// Função "Artista" da animação: Decide o que desenhar na tela.
static void animation_phase(void) {
    oled_set_cursor(0, 0);
    if (get_current_wpm() <= IDLE_SPEED) {
        current_idle_frame = (current_idle_frame + 1) % IDLE_FRAMES;
        oled_write_raw_P(idle[current_idle_frame], ANIM_SIZE);
    } else if (get_current_wpm() < TAP_SPEED) {
        oled_write_raw_P(prep[0], ANIM_SIZE);
    } else {
        current_tap_frame = (current_tap_frame + 1) % TAP_FRAMES;
        oled_write_raw_P(tap[current_tap_frame], ANIM_SIZE);
    }
}

// Função "Gerente" da animação: Gerencia o tempo e o estado do OLED.
static void render_anim(void) {
    // Apenas verifica se o OLED está ligado para desenhar a animação
    if (is_oled_on()) {
        if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION) {
            anim_timer = timer_read32();
            animation_phase();
        }
    }
}

// =============================================
// Tarefa principal do OLED
// =============================================
bool oled_task_user(void) {
    // --- Lógica de Timeout (agora para AMBOS os lados) ---
    // Se houver qualquer digitação, "acorda" o OLED e reseta o timer de inatividade.
    if (get_current_wpm() > 0) {
        oled_on();
        anim_sleep = timer_read32(); // Usa a variável global já existente
    }

    // Se o tempo de inatividade passou do limite, desliga o OLED.
    if (timer_elapsed32(anim_sleep) > oled_timeout) {
        oled_off();
    }

    // Se o OLED não estiver desligado, desenha o conteúdo de cada lado.
    if (is_oled_on()) {
        if (is_keyboard_master()) {
            render_status(); // Lado esquerdo (mestre) com layout vertical
        } else {
            render_anim();   // Lado direito (escravo) com animação
        }
    }

    return false;
}

// =============================================
// Funções de Teclas Personalizadas
// =============================================
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    static uint16_t caps_hold_timer;

    switch (keycode) {
        case CAPS_HOLD:
            if (record->event.pressed) {
                caps_hold_timer = timer_read();
            } else {
                if (timer_elapsed(caps_hold_timer) < TAPPING_TERM) {
                    tap_code(KC_NUBS); // Ação de TAP (toque rápido)
                } else {
                    tap_code(KC_CAPS); // Ação de HOLD (segurar)
                }
            }
            return false;
    }
    return true;
}
